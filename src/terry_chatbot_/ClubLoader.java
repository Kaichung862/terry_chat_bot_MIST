	 public ArrayList<Club> loadAllClubs() {
	        ArrayList<Club> clubs = new ArrayList<>();

	        for (int i = 0; i < terry_organization.length; i++) {
	            if (terry_organization[i].isEmpty() || contact_info[i].isEmpty()) continue;

	            String name = terry_organization[i];
	            String meeting = i < meeting_time.length ? meeting_time[i] : "Varies";
	            String career = i < associated_career_path.length ? associated_career_path[i] : "Any";
	            String membership = i < process_to_be_member.length ? process_to_be_member[i] : "Not specified";
	            String majorReq = i < requirements_major.length ? requirements_major[i] : "None";
	            String mistersRaw = i < misters_credit.length ? misters_credit[i] : "no";
	            String contact = i < contact_info.length ? contact_info[i] : "Not available";

	            boolean misters = mistersRaw.equalsIgnoreCase("yes") || mistersRaw.equalsIgnoreCase("sometimes");

	            Club club = new Club(name, meeting, career, membership, majorReq, misters, contact);
	            clubs.add(club);
	        }

	        return clubs;
	    }
